//String  문자열

const myName = "둘리"; // 큰 따옴표
const email = 'user@naver.com'; //작은 따옴표

const hello = `hello ${myName}`; //백틱, ${} 기호를 사용해서 문자열에 변수를 포함시킴

console.log(myName);
console.log(email);
console.log(hello);


//Number(숫자)
const number = 123;
const opacity = 0.57;
console.log(number);
console.log(opacity);

//Boolean(~여부, 논리형)
let checked = true;
let isShow = false;
console.log(checked);
console.log(isShow);

let undef; //값을 대입하지 않으면 undefined 출력
console.log(undef);

let name = null;  //개발자가 직접 대입
console.log(name);
